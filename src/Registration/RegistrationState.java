package Registration;

/**
 * The possible states of a registration process.
 */
public enum RegistrationState {
    
    /**
     * Started state
     */
    STARTED,
    
    /**
     * Filled state
     */
    FILLED,
    
    /**
     * Validated state
     */
    VALIDATED,
    
    /**
     * Payed state
     */
    PAYED,
    
    /**
     * Finished state
     */
    FINISHED,
    
    /**
     * Rejected state
     */
    REJECTED;
}
